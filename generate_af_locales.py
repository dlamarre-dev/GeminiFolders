NEW_KEYS_ORDER = [
    "newConv_gemini", "newConv_claude", "newConv_chatgpt",
    "newConv_copilot", "newConv_perplexity", "newConv_local",
    "setLocalUrl", "alertNotSupported", "alertEditorNotFound",
    "storageFullError",
]

def transform(lang, gf_msgs, af_overrides):
    gf_name = gf_msgs.get("extName", {}).get("message", "Gemini Folders")
    af_name = af_overrides["extName"]
    out = {}

    for key, val in gf_msgs.items():
        if key in KEYS_TO_REMOVE:
            continue
        entry = {"message": val["message"]}

        if key in ("extName", "appTitle"):
            entry["message"] = af_name
        elif key == "extDesc":
            entry["message"] = af_overrides["extDesc"]
        elif key == "promptInsertBtn":
            entry["message"] = af_overrides["promptInsertBtn"]
        elif key in ("ctxMenuSave", "toastSaved", "masterFolderName"):
            if key in af_overrides:
                entry["message"] = af_overrides[key]
            else:
                entry["message"] = val["message"].replace(gf_name, af_name)
        # all other shared keys pass through unchanged

        out[key] = entry

    # Add new AF-only keys at the end
    for k in NEW_KEYS_ORDER:
        out[k] = {"message": af_overrides[k]}

    return out


def main():
    langs = sorted(
        d for d in os.listdir(GF_LOCALES)
        if os.path.isdir(os.path.join(GF_LOCALES, d)) and d != "en"
    )

    generated = 0
    for lang in langs:
        gf_path = os.path.join(GF_LOCALES, lang, "messages.json")
        if not os.path.exists(gf_path):
            print(f"  SKIP {lang} — GF file not found")
            continue
        if lang not in AF:
            print(f"  SKIP {lang} — no AF translations defined")
            continue

        with open(gf_path, encoding="utf-8") as f:
            gf_msgs = json.load(f)

        af_msgs = transform(lang, gf_msgs, AF[lang])

        out_dir = os.path.join(AF_LOCALES, lang)
        os.makedirs(out_dir, exist_ok=True)
        out_path = os.path.join(out_dir, "messages.json")
        with open(out_path, "w", encoding="utf-8") as f:
            json.dump(af_msgs, f, ensure_ascii=False, indent=2)
            f.write("\n")

        print(f"  OK  {lang}")
        generated += 1

    print(f"\nDone — {generated} locale files written.")


if __name__ == "__main__":
    main()
